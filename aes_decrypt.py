#!/usr/bin/python3

from Cryptodome.Cipher import AES
import zlib

key = b'7c07f68ea8494b2f8b9fea297119350d'
iv = b'FEDCBA0987654321'

cipher_text = b'\x59\xce\xb3\x20\xe0\x8a\x5d\x4c\x53\x1c\xb1\x02\xb4\x78\x89\x85\xa9\x30\x7c\x9c\xf2\x5c\x55\x11\xd6\x53\x72\x2e\x20\x60\x8f\x57\xfd\x35\x52\xb7\x22\xf4\x63\x62\x1f\x08\xdf\xf5\xb7\xaf\x95\xe7\x3a\x2f\x5a\xa1\x45\xc8\x8c\x6a\x4a\xb7\x0b\xb9\x90\x6d\xd1\xa0\x6b\x08\x5c\xce\x37\xfe\x82\x99\x2a\x24\xc1\x2b\x54\xd7\x92\xc1'

aes = AES.new(key, iv=iv, mode=AES.MODE_CBC)
compressed_plaintext = aes.decrypt(cipher_text)

# Remove padding
while compressed_plaintext[0] == ord('!'):
    compressed_plaintext = compressed_plaintext[1:]

plain_text = zlib.decompress(compressed_plaintext).decode("utf-8")
print(plain_text)

